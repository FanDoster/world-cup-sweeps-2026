function svg(glowColor, glowWidth, glowOpacity) {
  return `<svg xmlns='http://www.w3.org/2000/svg' width='32' height='32' viewBox='0 0 32 32'>` +
    `<g transform='rotate(-14 16 16)'>` +
    `<path d='${HAND}' fill='none' stroke='${glowColor}' stroke-width='${glowWidth}' stroke-linejoin='round' opacity='${glowOpacity}'/>` +
    `<path d='${HAND}' fill='#fff' stroke='#223a70' stroke-width='1.4' stroke-linejoin='round'/>` +
    `<path d='M18.9 13.6V16.6M22.3 13.9V16.6' fill='none' stroke='#223a70' stroke-width='0.9' stroke-linecap='round' opacity='0.45'/>` +
    `</g></svg>`;
}

function toCursorUrl(s) {
  const enc = s
    .replace(/</g, '%3C')
    .replace(/>/g, '%3E')
    .replace(/#/g, '%23')
    .replace(/"/g, "'")
    .replace(/\s+/g, ' ');
  return `url("data:image/svg+xml,${enc}") 11 4`;
}

const idle = toCursorUrl(svg('#9cc8ff', 5, 0.85));      // soft P1-blue rim
const hover = toCursorUrl(svg('#54d8ff', 5.6, 0.95));   // brighter cyan glow over clickables

const css = `/* Wii Remote pointer cursor — white glove hand with the P1 blue glow.
   Generated by .claude/gen-wii-cursor.mjs (node .claude/gen-wii-cursor.mjs) — don't hand-edit the data URIs. */

* {
  cursor: ${idle}, auto !important;
}

/* Clickables get a brighter glow (covers the inline style="cursor:pointer" spans via [onclick]) */
a, button, select, label, summary,
[onclick], [role="button"] {
  cursor: ${hover}, pointer !important;
}

/* Keep the I-beam where you actually type */
input:not([type="button"]):not([type="submit"]):not([type="checkbox"]):not([type="radio"]):not([type="range"]):not([type="file"]),
textarea {
  cursor: text !important;
}

/* The shooter draws its own crosshair */
#shooter-canvas {
  cursor: none !important;
}
`;

writeFileSync(new URL('../css/wii-cursor.css', import.meta.url).pathname, css);
console.log('wrote css/wii-cursor.css');
